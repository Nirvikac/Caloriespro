class UserInfoEntity {
  final String userId;
  final int age;
  final double weight;
  final double height;
  final String gender;
  final String yourGoal;
  final String activityLevel;

  UserInfoEntity({
    required this.userId,
    required this.age,
    required this.weight,
    required this.height,
    required this.gender,
    required this.yourGoal,
    required this.activityLevel,
  });
}
